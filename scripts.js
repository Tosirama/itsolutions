const fb2 = document.getElementById('fb2');
const im2 = document.getElementById('im2');

fb2.addEventListener('click',function(){
    im2.style.height='300px';
    im2.style.width= '300px';


})

fb2.addEventListener('dblclick',function(){
    im2.style.height='0';
    im2.style.width= '0';
})

